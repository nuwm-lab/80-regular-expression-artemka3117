using System.Text.RegularExpressions;

// Заданий текст
string text = "Інформатика - це фундаментальна наука. Ми любимо вивчати інформатику. Інформатика відкриває нові горизонти. Чи знаєте ви, що інформатика є ключовою для технологій? Це просто чудово. Інформатика допомагає вирішувати складні задачі.";

// Регулярний вираз для пошуку речень, що починаються зі слова "Інформатика".
// (^|\\.\\s*|\\?\\s*|!\\s*) - шукає початок рядка, або крапку/знак питання/знак оклику з можливими пробілами після них.
// (Інформатика) - шукає слово "Інформатика".
string pattern = @"(?:^|\.\s*|\?\s*|!\s*)Інформатика";

// Виконуємо пошук збігів
MatchCollection matches = Regex.Matches(text, pattern);

// Виводимо результат
Console.WriteLine($"Знайдено {matches.Count} речень, що починаються зі слова 'Інформатика'.");
